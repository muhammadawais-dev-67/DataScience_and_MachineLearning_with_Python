
# Upper case 
x = "my name is awais and my father name is zulfi"
print(x.upper())

#Lowercase
x = "MY NAME IS Awais AND MY FATHER NAME IS Zulfi"
print(x.lower())

# Removing Whitespace

x = "   muhammad, Awais "
print(x.strip())  

# Replace string 

x = "This is data  science tutorials"
print(x.replace("This" , "Bahi"))

# Split string 

x = "This, is, data , science, tutorials"
print(x.split(","))



















